                Modern turkey breeding and farming practices have significantly reduced both the amount of feed 
                and the time required to produce a pound of turkey meat. In 12–14 weeks a hen turkey eats about 16 
                kg (35 pounds) of feed and reaches 6–9 kg (14–20 pounds). Toms require some 36 kg (80 pounds) of feed
                to reach a market weight of 16–19 kg (35–42 pounds) in 16–19 weeks. Smaller turkey broilers are marketed
                from 12 to 15 weeks of age. Turkeys can be raised on open land with automatic waterers, self-feeders,
                range shelters, heavy fencing, and rotated pastures; however, they are often “grown out” commercially
                in rearing houses under environmentally controlled conditions.
